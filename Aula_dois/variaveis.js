// Variáveis

// var é antigo, substituir por let, problemas de escopo.

// declaração
let idade = 19; // tipo inteiro -> (idade: Number)
let altura = 1.72 // tipo Number -> (altura: Number)
let nome = "Filipe" // tipo string -> (nome: String)
let estudando = true; // tipo boolean -> (estudando: boolean)

// Imprimindo variáveis e seus tipos
console.log(nome, typeof nome);
console.log(altura, typeof altura);
console.log(idade, typeof idade);
console.log(estudando, typeof estudando);

// Tipo indefinido
let outra = undefined; // também pode ser let outra;
console.log(outra);

// Tipo nulo
let nulo = null;
console.log(nulo);

// Constantes
const pi = 3.14;
console.log(pi);
